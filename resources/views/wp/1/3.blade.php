<div>
    <h3  class="pros-cons">Types of Stablecoins in the Modern Market</h3>

    <div class="pros-cons">
        <h3>1. Centralized Stablecoins (USDT, USDC, BUSD)</h3>
        <p>They are backed by fiat money and the equivalent in stocks, bonds, etc.</p>
        <h4>Positive:</h4>
        <ul>
            <li>The first of the stablecoins on the market.</li>
            <li>Large capitalization.</li>
            <li>Recognition and frequent presence on a large number of exchanges.</li>
        </ul>
        <h4>Minuses:</h4>
        <ul>
            <li>Centralization.</li>
            <li>The ability to block tokens at the smart contract level.</li>
            <li>There are big doubts about the sufficient security of the full issue.</li>
        </ul>
    </div>

    <div class="pros-cons">
        <h3>2. Stablecoins Backed by Cryptocurrency (DAI)</h3>
        <p>It is difficult to find other popular stablecoins of this type on the market.</p>
        <h4>Positive:</h4>
        <ul>
            <li>High decentralization and security (in the case of DAI. Although it is dependent on a centralized USDC,
                which provides about 32% of the total cost of the DAI issue. It is worth noting that Circle (the USDC
                Issuer) has allowed the possibility of blocking the collateral contained in the DAI smart contract).
            </li>
            <li>In the case of DAI, there is increased collateral, which negatively affects the profitability of the
                application and lower liquidity of the project for the issuer itself.</li>
        </ul>
        <h4>Minuses:</h4>
        <ul>
            <li>By participating in the DAI issue, users must contribute excess collateral to receive the required
                number of tokens. So, for example, in order to issue DAI in the equivalent of $100, it is necessary to
                deposit collateral, for example, in an Eth coin in the amount of at least $150, so that positions are
                not liquidated.</li>
            <li>High commissions of the Ethereum network.</li>
        </ul>
    </div>

    <div class="pros-cons">
        <h3>3. Algorithmic Stablecoins (USDD, USDN, UST)</h3>
        <p>The last two have radically lost their binding to $.</p>
        <h4>Positive:</h4>
        <ul>
            <li>High liquidity of the providing coin and its growth with the growth of the capitalization of the
                provided stablecoin.</li>
        </ul>
        <h4>Minuses:</h4>
        <ul>
            <li>The possibility of losing the link to the target fiat currency in case of sharp fluctuations in the
                cryptocurrency market and insufficient elaboration of security algorithms.</li>
        </ul>
    </div>

    <p>At the moment, the market does not lack representatives of the stablecoin ecosystem. There are really a lot of
        them. I can't even remember everything.</p>
    <p>However, as mentioned above, almost all of them have some flaws or even critical vulnerabilities that scammers or
        competitors sooner or later exploit.</p>
</div>
