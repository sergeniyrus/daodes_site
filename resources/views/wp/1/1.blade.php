<h1>Challenges and Solutions</h1>

<p>The losses of the global economy from cyber threats have more than tripled over the past 4 years and amounted to an
    estimated at least $8 trillion in 2022. By 2025, according to expert estimates, global losses could reach $11
    trillion, and by 2030 — $90 trillion. This makes it clear that it is unacceptable to strive to increase the
    scalability of the blockchain by reducing security! In turn, less decentralized projects have greatly discredited
    themselves by their high centralization and the pursuit of interests primarily of the so-called elites of the
    project, rather than blockchain communities and libertarian ideals. It turns out to be a kind of
    "pseudo-centralization". In the eyes of the community, such projects are gaining less and less trust and support for
    development. Investors are also not eager to develop blockchain projects in which the power of decisive votes is
    concentrated in the hands of centralized structures (whether they are dominant mining pools of PoW networks, large
    validators of PoS/DPoS, etc. networks, or the concentration of a large% of project tokens received by the creators
    or investors of the project in the early stages of project development at very low prices).</p>

<p>At the moment, there are many players on the market providing solutions to 2 of the 3 problems of the blockchain
    scalability trilemma, and they can be broken down by consensus types:</p>

<div class="pros-cons">
    <h2>Projects with PoW Consensus</h2>
    <h3>Advantages:</h3>
    <ul>
        <li>High (relatively) decentralization and network security.</li>
    </ul>
    <h3>Cons:</h3>
    <ul>
        <li>Low scalability. As the volume of requests to the network increases, the fees increase. And this
            disadvantage, without work on leveling it, will not allow PoW networks to become a large–scale participant
            in WEB3.0 networks, since billions and trillions of transactions will be needed for their operation, and
            preferably with fees tending to zero. That is, the number of transactions is comparable to the bandwidth of
            centralized servers.</li>
    </ul>
</div>

<div class="pros-cons">
    <h2>Projects with PoS/DPoS Consensus and Others Without Proof of Work</h2>
    <h3>Advantages:</h3>
    <ul>
        <li>High scalability and fairly high network security.</li>
        <li>High environmental friendliness compared to networks running on the PoW consensus, since miners do not have
            to participate in the "arms race". They become validators by proving their stake in the project, rather than
            providing the most computing power with all the consequences.</li>
    </ul>
    <h3>Cons:</h3>
    <ul>
        <li>High centralization, since even with the stated decentralization of the system, most often the maximum
            voting power is concentrated in the hands of the largest validators with large stakes. The interests of
            decentralization are far from their first place, and most often the question of decentralization is not at
            all, and only the initially declared management structure has the right to vote.</li>
    </ul>
</div>

<p>At the same time, the problems of centralization of cryptocurrency projects expose them to criticism, investment
    outflow and a negative information field for the entire cryptocurrency industry.</p>

<p>At the moment, there are no projects on the market that have completely solved the blockchain scalability trilemma.
</p>
