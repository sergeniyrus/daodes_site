  <p>Currently, there are a lot of decentralized exchanges, however, they have only in the last few months begun to approach centralized exchanges in their functionality, without losing the advantages of decentralization.</p>
  <p>(There are only a few of them in the Cosmos ecosystem, and they are under development or testing.) Existing exchanges are quite different from each other and have their advantages and disadvantages, we have summarized the weaknesses and strengths of existing DEXs involved in the implementation of the following functionality of centralized exchanges:</p>

  <div class="pros-cons">
      <h3>Positive:</h3>
      <ul>
          <li>Anonymity.</li>
          <li>Lower fees compared to centralized (CEX) exchanges.</li>
          <li>The absence of intermediaries, which are centralized exchanges (CEX): Binance, Exmo and others. This increases the level of security by returning control of assets to the parties involved. Transactions between traders on DEX exchanges are carried out using verification through multi-signatures.</li>
      </ul>
  </div>

  <div class="pros-cons">
      <h3>Minuses:</h3>
      <ul>
          <li>The absence of many options for traders, such as margin trading or the choice of the order mode for the vast majority of representatives of this type of exchanges. (Exchanges are actively developing in this direction, but products from strong projects are still in the testing process).</li>
          <li>Many DEXs operate on smart contracts, so only tokens with support for smart contracts of the respective networks can be traded on them.</li>
          <li>There is an acute issue with liquidity.</li>
      </ul>
  </div>

  <p>Thus, at the moment, DEX exchanges are solving the problem of decentralization and low fees, however, in terms of convenience, speed and functionality, they are very much losing to their centralized competitors, not being able to solve the lion's share of the tasks that centralized exchanges solve and which have long been very firmly embedded in the lives of crypto investors and enthusiasts. It is precisely such a DEX exchange (simplifying the user experience and bringing it closer in functionality to CEX) that the DES project plans to develop in the future, after attracting investments and achieving initial goals.</p>
