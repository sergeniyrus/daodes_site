  <h2>Investment Opportunities for Network Participants</h2>
  <p>Each network participant has the opportunity to invest their DES coins (or DESStable stablecoins) in one of the products that are under development by DAODES or can be implemented in the DES project (on the project's blockchain). In this case, investments are made only in the project's stablecoins.</p>

  <h3>Benefits for Investors</h3>
  <ul>
      <li><strong>Transaction Fee Share:</strong> When staking coins into a product/DAO, the investor will receive a portion of the transaction fee (turnover) of this product.</li>
      <li><strong>Investment Flexibility:</strong> The investor can choose to invest in a specific product or in the entire line of the DAO, delegating their funds to the project management for distribution among products as needed, at the discretion of the DAO.</li>
      <li><strong>Management Tokens:</strong> When investing in a specific product, the investor receives management tokens of that product, allowing them to participate in voting on initiatives within the DAO. The number of management tokens is distributed based on the investor's contribution relative to the total amount required to start the project. Investments account for 21% of the total issue of management tokens, while developers and activists distribute the remaining 79% based on their contributions to the project.</li>
  </ul>

  <h3>Step-by-Step Investment Mechanism</h3>
  <p>A step-by-step investment attraction mechanism has been implemented:</p>
  <ol>
      <li><strong>Initial Stage:</strong> Tokens are distributed to investors, with no more than 21% of the total volume of management tokens allocated to investors. Each investor can receive no more than $1,000 worth of tokens in this stage.</li>
      <li><strong>Subsequent Stage:</strong> The remaining 79% of management tokens are distributed without limiting the maximum number of tokens per investor.</li>
  </ol>

  <h3>Future Development</h3>
  <p>For the future, a pool will be created for each product/DAO:</p>
  <ul>
      <li><strong>Fund Allocation:</strong> Part of the raised funds will be deposited into this pool, along with contributions from the general community pool (part of the main DAO, which is divided among all products and branches of the DAO). This serves as a support fund for applications that have not yet been implemented. As the number of products increases, this pool will grow.</li>
      <li><strong>Smart Contract Freezing:</strong> During the investment attraction phase, all funds raised are frozen in a special smart contract. If the target fundraising level is reached, the funds are distributed according to the predefined plan. If the target is not met within the specified timeframe, the funds are returned to the investors' wallets.</li>
  </ul>

  <h3>Important Discussion Points</h3>
  <ul>
      <li><strong>Investor Participation in Unlocking DES Tokens:</strong> A discussion is ongoing about whether investors should receive an additional annual percentage of their invested funds, with 500 million coins allocated for this purpose.</li>
      <li><strong>Post-Launch Remuneration:</strong> After the launch of an application or project, developers, activists, and investors receive remuneration in DES tokens, equivalent to the management tokens of the specific DAO, from the transaction fees of that application.</li>
      <li><strong>Path to Parent DAO Management Tokens:</strong> Over time, holding management tokens of a product (regardless of how they were obtained: as a developer, investor, or activist) and meeting certain conditions (such as contributing to other areas of network development) will allow a participant to receive management tokens in the parent DAO DES.</li>
  </ul>

  <h3>Participant Statistics and DAO Membership</h3>
  <p>Participant statistics are collected using SBT (Soulbound Tokens via smart contracts). When certain achievements are met, the participant's candidacy is first proposed to the community for consideration. With sufficient positive feedback, it proceeds to a vote among DAO members for acceptance into the governing body. This process ensures that the community plays a key role in shaping the leadership of the DAO.</p>
