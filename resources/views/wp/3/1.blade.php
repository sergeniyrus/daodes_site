  <h2>Main Features</h2>
  <p>The DES project token, the DES ticker, is a utility coin of the project, which is used for:</p>
  <ul>
      <li><strong>Algorithmic provision of 25% of the value of the DES project algorithmic stablecoin ecosystem:</strong> Ensures the stability and backing of the stablecoin ecosystem.</li>
      <li><strong>Collection of transaction fees in the blockchain:</strong> Used to incentivize network participants and maintain the blockchain infrastructure.</li>
      <li><strong>Rewards for project validators:</strong> Validators are compensated for securing the network and validating transactions.</li>
      <li><strong>Bounty payments for project promotion activists:</strong> Rewards for community members who actively promote and contribute to the project.</li>
      <li><strong>Filling the DES coin's liquidity pool on decentralized exchanges:</strong> Ensures liquidity and trading availability of the DES token.</li>
      <li><strong>Validators' remuneration:</strong> Additional incentives for validators to maintain network integrity.</li>
      <li><strong>Filling of funds for current and insurance needs of the project:</strong> Supports operational and contingency requirements of the ecosystem.</li>
  </ul>

  <h3>Token Issuance</h3>
  <p>The issue of the DES token is a limited one-time issue. The issue volume is 1 billion coins, distributed as follows:</p>
  <ol>
      <li><strong>200 million coins:</strong> Issued once and locked on a smart contract for the following needs:
          <ul>
              <li><strong>Filling the liquidity pools of the main coin of the project (DES):</strong> 40 million coins.</li>
              <li><strong>Insurance funds of the project:</strong> 80 million coins.</li>
              <li><strong>Bounty mailing to opinion leaders:</strong> 20 million coins (targeted at individuals unlikely to engage in early coin sales).</li>
              <li><strong>The reward of the creators of the project:</strong> 20 million coins (step-by-step vesting based on a smart contract, considering market conditions and minimum desired value).</li>
              <li><strong>40 million coins:</strong> Allocation to be decided through discussion by the active and effective community of the project.</li>
          </ul>
      </li>
      <li><strong>200 million coins:</strong> Stored in a smart contract to ensure the release of new stablecoins.</li>
      <li><strong>100 million coins:</strong> Distributed in investment rounds to attract early funding and support project development.</li>
      <li><strong>500 million coins:</strong> Distributed among the project's activists as a reward for initiating, developing, and implementing proposals aimed at improving the project, which will be accepted by the entire community.</li>
  </ol>
