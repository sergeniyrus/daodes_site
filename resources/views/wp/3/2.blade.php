  <h2>Token Implementation During Investment Rounds</h2>
  <p>The implementation of tokens during the investment rounds for project development will occur in two stages:</p>

  <h3>First Stage: Pre-Launch (Before Token Listing on Exchanges)</h3>
  <p>At the first stage, 100,000,000 DES will be issued, which is 10% of the total issuance. The first stage will be conducted in accordance with 3 rounds of the initial offering:</p>
  <ol>
      <li><strong>1st Round of Initial Offering:</strong> Involves the placement of 10,000,000 DES, which is 1% of the total issuance, at a price of $0.01, raising $100,000. In the first round, the cost of project implementation, main products, and the roadmap are calculated.</li>
      <li><strong>2nd Round of Initial Offering:</strong> Involves the placement of 50,000,000 DES, which is 5% of the total issuance, at a price of $0.05, raising $2,500,000. In the second round, the launch of our blockchain and the start of work on the products take place.</li>
      <li><strong>3rd Round of Initial Offering:</strong> Involves the placement of 40,000,000 DES, which is 4% of the total issuance, at a price of $0.1, raising $4,000,000. Preparation for listing on exchanges, expansion of the user network, formation of the validator network, and project promotion occur during this round.</li>
  </ol>
  <p>The transaction fee at the first stage across all 3 rounds will be 0.1% of the transaction amount.</p>

  <h3>Second Stage: Standard Coin Mining in Accordance with Consensus</h3>
  <p>For the completion of this stage, 500 million DES coins will be reserved in the pool. Mining will occur through:</p>
  <ol>
      <li><strong>Validation:</strong> Rewards for validating transactions and securing the network.</li>
      <li><strong>Delegation:</strong> Rewards for delegating tokens to validators.</li>
      <li><strong>Completion of Project Tasks:</strong> Rewards for completing tasks and objectives set by the project.</li>
  </ol>
