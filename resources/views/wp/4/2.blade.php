    <h2>Encouragement of Validators and Delegators</h2>
    <p>Validators and delegators are incentivized to ensure business continuity and liquidity through a modified DPoS
        consensus mechanism. The system is designed to reward both validators and delegators based on their
        contributions to the network.</p>

    <h3>Validator and Delegator Pools</h3>
    <ul>
        <li><strong>Delegator Pool (P1):</strong> Delegators contribute their tokens to a general pool (P1). The voting
            power of this pool is distributed among validators based on their Uptime (the number of blocks signed
            consecutively without omissions, fines, or malicious actions) over a period of 10,000 blocks. Validators are
            ranked, and their voting power is updated periodically based on their performance.</li>
        <li><strong>Validator Pool (P2):</strong> Transaction fees collected during the 10,000-block period are placed
            in a separate pool (P2). This pool is split 50/50 between validators and delegators.</li>
    </ul>

    <h3>Earnings Distribution</h3>
    <ul>
        <li><strong>Validators:</strong> Validators earn rewards based on the number of blocks they sign. For every
            signed block, validators receive 0.01% of the total rewards pool (P2).</li>
        <li><strong>Delegators:</strong> Delegators receive 50% of the rewards pool (P2), distributed proportionally
            based on their share of the total delegator pool (P1). The system adjusts in real-time to account for
            changes in the delegator pool during the 10,000-block period.</li>
    </ul>
