  <h2>Task Exchange: Mining DES Coins for Project Benefits</h2>
  <p>To implement the process of mining DES coins for bringing benefits to the project, a section of the site called "Task Exchange" is being created. All actions in this section are summarized and transferred to the blockchain for subsequent accrual of activity points.</p>

  <h3>Task Exchange Overview</h3>
  <ul>
      <li><strong>Forum for Proposals:</strong> The Task Exchange serves as a forum for making suggestions and includes tools for voting on community proposals.</li>
      <li><strong>Task Rating System:</strong> Community proposals supported by likes from at least 10% of the voting power of the project's activists are transferred to the Task Exchange rating. Tasks are prioritized based on community voting, with higher-priority tasks receiving funding sooner.</li>
  </ul>

  <h3>Task Assignment and Payment</h3>
  <ul>
      <li><strong>Market Review:</strong> Experts assign the maximum payment amount and requirements for the contractor's competencies and experience for each task.</li>
      <li><strong>Competitive Bidding:</strong> If multiple applicants meet the requirements, the task is assigned to the applicant who agrees to complete it for the lowest cost.</li>
      <li><strong>Price Adjustment:</strong> If no applicants are found, the task's price increases by 10% every 72 hours until a performer is found or the task is removed from the exchange.</li>
  </ul>

  <h3>Funding and Rewards</h3>
  <ul>
      <li><strong>Fixed DES Coin Issuance:</strong> Every 10,000 blocks, a fixed number of DES coins are issued. These coins are exchanged for stablecoins and sent to a pool for rewarding task performers.</li>
      <li><strong>Task Execution:</strong> Only tasks at the top of the priority rating with sufficient funds can be offered for execution. Performers receive stablecoins equivalent to the agreed payment amount upon successful completion of the task.</li>
      <li><strong>Matching Bonus:</strong> In addition to the payment, performers receive a matching bonus based on their role (e.g., programmer, marketer, polyglot, copywriter, blogger, visualizer).</li>
  </ul>

  <h3>Path to Governance</h3>
  <p>Active participants who consistently contribute to the project through the Task Exchange can become full members of the project's governing council, gaining a voice in decision-making processes.</p>
