<h2>Third-generation Decentralized Blockchain</h2>
<p>The Decentralized EcoSystems project is being created with the aim of solving the blockchain scaling trilemma,
    which means creating a blockchain network that, without questioning security and decentralization, will ensure
    scalability and network speed comparable to centralized servers, necessary for the operation of Web3 and dApps
    networks and their usability at a comparable level with centralized networks and applications.</p>
<p>To solve this problem, the fork of the Cosmos Network project (DPoS consensus) is used as the basis.</p>
<p>Given that there were no problems with the security of the Cosmos network, the possibilities of increasing the
    decentralization and scalability of this network were explored.</p>

<h3>Features of DecentralizeDPoS Consensus:</h3>
<ol>
    <li>An additional Proof-of-Time algorithm is added (based on the time spent in the validator network). Thus,
        validators are encouraged to be as technically compliant as possible with the requirements of the network.
    </li>
    <li>The network management module is being upgraded: the highest governing body of the network is the DAO
        (decentralized autonomous organization), in which the most active participants of the decentralized network
        have the greatest voting weight, not the holders of the largest pool, but the most active participants in the
        decentralized network, with a maximum share limit of 8%, which encourages network participants to participate
        more actively in the functioning and development of the network towards decentralization and socialization.
    </li>
</ol>

<h3>To increase the scalability of the projected network, a sharding mechanism will be implemented in the source code
    of the Cosmos blockchain.</h3>
<p>Sharding will be implemented as follows:</p>
<ol>
    <li>The concept of a general validator is introduced. This status is assigned to a node that acts as a validator
        when approving a block with its group (shard).</li>
    <li>Based on the Proof of Time algorithm, a node fault tolerance rating is introduced. The longer the uptime and
        the absence of penalties (for work not done or not online), the higher the node's rating.</li>
    <li>The most reliable nodes get the right to become general validators of shards.</li>
    <li>All network validators are randomly pre-grouped into groups of 5-15 nodes every (1-15 is specified during the
        testnet process) seconds, and by the time the block is validated, they are formed (into shards), and they are
        in a fail-safe state and ready to quickly and safely verify and approve (or reject) the proposed block.</li>
</ol>

<p>Thus, due to the announced technical solutions, the DES blockchain will be as close as possible to solving the
    blockchain scaling trilemma, since it will be able to conduct the number of transactions as close as possible to
    the fastest centralized systems without compromising the decentralization and security of the network.</p>
<p>Considering these technical scaling solutions, it was decided to make the fees minimal (the transaction fee will
    range from 0.1% to 0.02% of the transaction amount. A detailed description can be found in the DES token issue
    section). Due to these factors, it will be easy and profitable to create high-performance WEB3 applications based
    on the DES blockchain.</p>
<p>To support novice application developers, at the initial stage of the project, the transaction fee will be
    increased to 0.1% and will decrease as the number of transactions increases to the projected commission amount of
    0.02%.</p>
<p>Also, this transaction fee will be related to the total number of transactions in the DES ecosystem. As the number
    of transactions and capitalization increase, the cost of transactions will decrease.</p>

<h3>The difference between the initial commission (0.1%) and the project commission (0.02%) will be distributed as
    follows:</h3>
<ol>
    <li>Reward app creators (30%)</li>
    <li>Remuneration of validators (20%)</li>
    <li>Operating expenses (25%)</li>
    <li>Filling the liquidity pool of the main coin of the project (DES) (10%)</li>
    <li>Project Development Fund (10%)</li>
    <li>Replenishment of the activist reward pool. (5%)</li>
</ol>

